# Tuplas

tupla =  ("San Bernardo", 12, 3.1416, True)
print(tupla)

# Acceder a los elementos de la tupla

print(tupla[0])
print(tupla[1])
print(tupla[2])
print(tupla[3])

# Tambien sirve los numeros negativos para revisar la tupla desde el ultimo elemento al primero

# Cuantas veces se repite un elemento

print(tupla.count(True))

# Ver la primera aparicion de un elemento de la tupla

print(tupla.index(12))

# Una tupla de un elemento se toma como el elemento en si
print((1))

# Para hacer que python la tome como tupla tenemos que ponerle una coma
print((2, ))
